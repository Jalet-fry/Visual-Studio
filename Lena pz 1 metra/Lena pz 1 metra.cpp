#include <iostream>
#include <vector>
#include <algorithm> // Для std::reverse
#include <limits>    // Для очистки буфера ввода

// Функция для проверки корректности ввода числа
int inputInteger() {
    int value;
    while (true) {
        std::cin >> value;
        if (std::cin.fail()) {  // Проверка на ошибку ввода
            std::cin.clear();
            std::cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n');
            std::cout << "Ошибка ввода! Введите целое число: ";
        } else {
            return value;  // Возврат корректного значения
        }
    }
}

// Основная функция программы
int main() {
    int N;
    std::cout << "Введите размер массива (N): ";
    N = inputInteger();

    if (N < 0 || N > 100) {
        std::cout << "Недопустимый размер массива!" << std::endl;
        return 1;
    }

    std::vector<int> arr(N);
    std::cout << "Введите элементы массива: ";
    for (int i = 0; i < N; ++i) {
        arr[i] = inputInteger();
    }

    // Логика нахождения и перестановки элементов
    int min_idx = 0, max_idx = 0;
    for (int i = 1; i < N; ++i) {
        if (arr[i] < arr[min_idx]) min_idx = i;
        if (arr[i] > arr[max_idx]) max_idx = i;
    }

    if (min_idx > max_idx) std::swap(min_idx, max_idx);
    if (min_idx + 1 < max_idx) std::reverse(arr.begin() + min_idx + 1, arr.begin() + max_idx);

    // Вывод результата
    std::cout << "Преобразованный массив: ";
    for (int i = 0; i < N; ++i) std::cout << arr[i] << " ";
    std::cout << std::endl;

    return 0;
}


